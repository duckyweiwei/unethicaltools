// Conservative, framework-safe response headers applied to every route. No strict
// CSP here on purpose: Next's inline runtime, Tailwind, and Vercel Analytics make a
// tight policy a breakage risk that needs its own dedicated pass. HSTS is set
// without includeSubDomains/preload so it can't strand sibling hosts.
const securityHeaders = [
  { key: "X-Content-Type-Options", value: "nosniff" },
  { key: "X-Frame-Options", value: "SAMEORIGIN" },
  { key: "Referrer-Policy", value: "strict-origin-when-cross-origin" },
  { key: "Permissions-Policy", value: "camera=(), microphone=(), geolocation=()" },
  { key: "Strict-Transport-Security", value: "max-age=31536000" },
];

/** @type {import('next').NextConfig} */
const nextConfig = {
  reactStrictMode: true,
  async headers() {
    return [{ source: "/:path*", headers: securityHeaders }];
  },
};

export default nextConfig;
